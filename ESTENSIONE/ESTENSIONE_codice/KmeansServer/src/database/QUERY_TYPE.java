package database;

/**
 * Enumerazione delle variabili di aggregazione di MYSQL
 */
public enum QUERY_TYPE {
    /**
     * Aggregatore di minimo di MYSQL, recupera il valore minimo da una tabella
     */
    MIN,
    /**
     * Aggregatore di massimo di MYSQL, recupera il valore massimo da una tabella
     */
    MAX
}
